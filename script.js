// Function to display items on the page
function displayItems(itemsArray) {
    const contentDiv = document.getElementById("content");
    contentDiv.innerHTML = ""; // Clear existing items

    itemsArray.forEach(item => {
        const galleryDiv = document.createElement("div");
        galleryDiv.classList.add("gallery");
        galleryDiv.innerHTML = `
            <a href="${item.link}">
                <img src="${item.img}" alt="${item.name}">
            </a>
            <div class="description">${item.name}</div>
        `;
        contentDiv.appendChild(galleryDiv);
    });
}

// Detect which page we are on and load the correct items
if (window.location.pathname.includes("main.html")) {
    displayItems([...kdramaItems, ...cdramaItems, ...otherItems]);
} else if (window.location.pathname.includes("kDrama.html")) {
    displayItems(kdramaItems);
} else if (window.location.pathname.includes("cDrama.html")) {
    displayItems(cdramaItems);
} else if (window.location.pathname.includes("others.html")) {
    displayItems(otherItems);
}

function searchItems() {
    const input = document.getElementById('searchInput');
    const filter = input.value.toLowerCase();
    const galleryItems = document.querySelectorAll('.gallery');
    const searchResultsHeader = document.getElementById('searchResults');

    // Reset search results header
    if (filter) {
        searchResultsHeader.textContent = `Results for: "${filter}"`;
    } else {
        searchResultsHeader.textContent = ''; // Clear the header if input is empty
    }

    let resultsFound = false; // Track if any results are found

    galleryItems.forEach(item => {
        const title = item.querySelector('.description').textContent;
        if (title.toLowerCase().includes(filter)) {
            item.style.display = ""; // Show item
            resultsFound = true; // Found at least one result
        } else {
            item.style.display = "none"; // Hide item
        }
    });

    // If no results were found, indicate that
    if (!resultsFound) {
        searchResultsHeader.textContent = `No results found for: "${filter}"`;
    }
}

